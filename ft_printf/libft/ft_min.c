#include "libft.h"

int			ft_min(int *tab, size_t size)
{
	size_t	i;
	int		nb;

	i = 0;
	nb = tab[0];
	i++;
	while (i < size)
	{
		if (nb > tab[i])
			nb = tab[i];
		i++;
	}
	return (nb);
}
