#include "libft.h"

void				*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*src;

	i = 0;
	src = (unsigned char *)s;
	while (i < n)
	{
		if ((unsigned char)src[i] == (unsigned char)c)
			return ((unsigned char *)&src[i]);
		i++;
	}
	return (NULL);
}
