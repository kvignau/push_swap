#include "libft.h"

size_t		ft_nbwordsspace(char const *s)
{
	size_t	i;
	size_t	j;
	int		ok;

	i = 0;
	j = 0;
	ok = 1;
	while (s[i])
	{
		if ((s[i] != ' ' && s[i] != '\t') && ok)
		{
			ok = 0;
			j++;
		}
		else if ((s[i] == ' ' || s[i] == '\t'))
			ok = 1;
		i++;
	}
	return (j);
}
